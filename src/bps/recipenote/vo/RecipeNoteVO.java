package bps.recipenote.vo;

public class RecipeNoteVO {
    private int note_no;
    private String note_date;
    private String note_con;
    private int rec_no;
    private String mem_id;
    public int getNote_no() {
        return note_no;
    }
    public void setNote_no(int note_no) {
        this.note_no = note_no;
    }
    public String getNote_date() {
        return note_date;
    }
    public void setNote_date(String note_date) {
        this.note_date = note_date;
    }
    public String getNote_con() {
        return note_con;
    }
    public void setNote_con(String note_con) {
        this.note_con = note_con;
    }
    public int getRec_no() {
        return rec_no;
    }
    public void setRec_no(int rec_no) {
        this.rec_no = rec_no;
    }
    public String getMem_id() {
        return mem_id;
    }
    public void setMem_id(String mem_id) {
        this.mem_id = mem_id;
    }
    
}
