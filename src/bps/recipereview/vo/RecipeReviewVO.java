package bps.recipereview.vo;

import java.sql.Date;

public class RecipeReviewVO {
	private int rrev_no   ;
	private Date rrev_date ;
	private int rrev_score;
	private String mem_id    ;
	private int rec_no    ;
	private String rrev_con  ;
	private String rec_name;
	
	public int getRrev_no() {
		return rrev_no;
	}
	public void setRrev_no(int rrev_no) {
		this.rrev_no = rrev_no;
	}
	public Date getRrev_date() {
		return rrev_date;
	}
	public void setRrev_date(Date rrev_date) {
		this.rrev_date = rrev_date;
	}
	public int getRrev_score() {
		return rrev_score;
	}
	public void setRrev_score(int rrev_score) {
		this.rrev_score = rrev_score;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public int getRec_no() {
		return rec_no;
	}
	public void setRec_no(int rec_no) {
		this.rec_no = rec_no;
	}
	public String getRrev_con() {
		return rrev_con;
	}
	public void setRrev_con(String rrev_con) {
		this.rrev_con = rrev_con;
	}
	public String getRec_name() {
		return rec_name;
	}
	public void setRec_name(String rec_name) {
		this.rec_name = rec_name;
	}
	
}
