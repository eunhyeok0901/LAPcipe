package bps.classdetail.vo;

public class ClassDetailVO {
    private int det_no;
    private String det_image;
    private String det_name;
    private String det_con;
    private int class_no;
    public int getDet_no() {
        return det_no;
    }
    public void setDet_no(int det_no) {
        this.det_no = det_no;
    }
    public String getDet_image() {
        return det_image;
    }
    public void setDet_image(String det_image) {
        this.det_image = det_image;
    }
    public String getDet_name() {
        return det_name;
    }
    public void setDet_name(String det_name) {
        this.det_name = det_name;
    }
    public String getDet_con() {
        return det_con;
    }
    public void setDet_con(String det_con) {
        this.det_con = det_con;
    }
    public int getClass_no() {
        return class_no;
    }
    public void setClass_no(int class_no) {
        this.class_no = class_no;
    }
    
}
