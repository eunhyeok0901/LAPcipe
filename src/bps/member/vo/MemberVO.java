package bps.member.vo;

public class MemberVO {

    private String mem_id;
    private String mem_nickname;
    private String mem_email;
    private String mem_tel;
    private String mem_bir;
    private String mem_add;
    private String mem_pass;
    private String mem_name;
    private int mem_recipe;
    private int mem_point;
    
    public String getMem_id() {
        return mem_id;
    }
    public void setMem_id(String mem_id) {
        this.mem_id = mem_id;
    }
    public String getMem_nickname() {
        return mem_nickname;
    }
    public void setMem_nickname(String nickname) {
        this.mem_nickname = nickname;
    }
    public String getMem_email() {
        return mem_email;
    }
    public void setMem_email(String mem_email) {
        this.mem_email = mem_email;
    }
    public String getMem_tel() {
        return mem_tel;
    }
    public void setMem_tel(String mem_tel) {
        this.mem_tel = mem_tel;
    }
    public String getMem_bir() {
        return mem_bir;
    }
    public void setMem_bir(String mem_bir) {
        this.mem_bir = mem_bir;
    }
    public String getMem_add() {
        return mem_add;
    }
    public void setMem_add(String mem_add) {
        this.mem_add = mem_add;
    }
    public String getMem_pass() {
        return mem_pass;
    }
    public void setMem_pass(String mem_pass) {
        this.mem_pass = mem_pass;
    }
    public String getMem_name() {
        return mem_name;
    }
    public void setMem_name(String mem_name) {
        this.mem_name = mem_name;
    }
    public int getMem_recipe() {
        return mem_recipe;
    }
    public void setMem_recipe(int mem_recipe) {
        this.mem_recipe = mem_recipe;
    }
    public int getMem_point() {
        return mem_point;
    }
    public void setMem_point(int mem_point) {
        this.mem_point = mem_point;
    }
    

    @Override
    public String toString() {
        return "MemberVO [mem_id=" + mem_id + ", mem_nickname=" + mem_nickname + ", mem_email=" + mem_email
                + ", mem_tel=" + mem_tel + ", mem_bir=" + mem_bir + ", mem_add=" + mem_add + ", mem_pass=" + mem_pass
                + ", mem_name=" + mem_name + ", mem_recipe=" + mem_recipe + ", mem_point=" + mem_point + "]";
    }

}
