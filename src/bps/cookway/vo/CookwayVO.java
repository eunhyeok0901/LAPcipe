package bps.cookway.vo;

public class CookwayVO {
	private int cook_no;
	private String cook_image;
	private String cook_con;
	private int rec_no;
	private String cook_index;
	private int id;
	public int getCook_no() {
		return cook_no;
	}
	public void setCook_no(int cook_no) {
		this.cook_no = cook_no;
	}
	public String getCook_image() {
		return cook_image;
	}
	public void setCook_image(String cook_image) {
		this.cook_image = cook_image;
	}
	public String getCook_con() {
		return cook_con;
	}
	public void setCook_con(String cook_con) {
		this.cook_con = cook_con;
	}
	public int getRec_no() {
		return rec_no;
	}
	public void setRec_no(int rec_no) {
		this.rec_no = rec_no;
	}
	public String getCook_index() {
		return cook_index;
	}
	public void setCook_index(String cook_index) {
		this.cook_index = cook_index;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
}
