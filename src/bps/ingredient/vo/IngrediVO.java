package bps.ingredient.vo;

public class IngrediVO {
  
    private String ing_name;
    private String ing_effect;
    private String ing_buy;
    private String ing_way;
    
    public String getIng_name() {
        return ing_name;
    }
    public void setIng_name(String ing_name) {
        this.ing_name = ing_name;
    }
    public String getIng_effect() {
        return ing_effect;
    }
    public void setIng_effect(String ing_effect) {
        this.ing_effect = ing_effect;
    }
    public String getIng_buy() {
        return ing_buy;
    }
    public void setIng_buy(String ing_buy) {
        this.ing_buy = ing_buy;
    }
    public String getIng_way() {
        return ing_way;
    }
    public void setIng_way(String ing_way) {
        this.ing_way = ing_way;
    }
}
