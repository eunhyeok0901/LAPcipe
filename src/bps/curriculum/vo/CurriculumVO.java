package bps.curriculum.vo;

public class CurriculumVO {
    private int cur_no;
    private String cur_name;
    private String cur_time;
    private int class_no;
    private String cur_video;
    public int getCur_no() {
        return cur_no;
    }
    public void setCur_no(int cur_no) {
        this.cur_no = cur_no;
    }
    public String getCur_name() {
        return cur_name;
    }
    public void setCur_name(String cur_name) {
        this.cur_name = cur_name;
    }
    public String getCur_time() {
        return cur_time;
    }
    public void setCur_time(String cur_time) {
        this.cur_time = cur_time;
    }
    public int getClass_no() {
        return class_no;
    }
    public void setClass_no(int class_no) {
        this.class_no = class_no;
    }
    public String getCur_video() {
        return cur_video;
    }
    public void setCur_video(String cur_video) {
        this.cur_video = cur_video;
    }
    
    
    
}
