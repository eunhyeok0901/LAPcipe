package bps.use_food.vo;

public class Use_foodVO {
	private int use_no   ;
	private int rec_no   ;
	private String ing_name ;
	private int ing_qty  ;
	private int id;
	public int getUse_no() {
		return use_no;
	}
	public void setUse_no(int use_no) {
		this.use_no = use_no;
	}
	public int getRec_no() {
		return rec_no;
	}
	public void setRec_no(int rec_no) {
		this.rec_no = rec_no;
	}
	public String getIng_name() {
		return ing_name;
	}
	public void setIng_name(String ing_name) {
		this.ing_name = ing_name;
	}
	public int getIng_qty() {
		return ing_qty;
	}
	public void setIng_qty(int ing_qty) {
		this.ing_qty = ing_qty;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	
}
