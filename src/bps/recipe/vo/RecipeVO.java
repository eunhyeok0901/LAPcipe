package bps.recipe.vo;

public class RecipeVO {
	private int rec_no;
	private String rec_name;
	private String rec_con;
	private String rec_video;
	private String rec_car;
	private int rec_count;
	
	
	public int getRec_no() {
		return rec_no;
	}
	public void setRec_no(int rec_no) {
		this.rec_no = rec_no;
	}
	public String getRec_name() {
		return rec_name;
	}
	public void setRec_name(String rec_name) {
		this.rec_name = rec_name;
	}
	public String getRec_con() {
		return rec_con;
	}
	public void setRec_con(String rec_con) {
		this.rec_con = rec_con;
	}
	public String getRec_video() {
		return rec_video;
	}
	public void setRec_video(String rec_video) {
		this.rec_video = rec_video;
	}
	public String getRec_car() {
		return rec_car;
	}
	public void setRec_car(String rec_car) {
		this.rec_car = rec_car;
	}
	public int getRec_count() {
		return rec_count;
	}
	public void setRec_count(int rec_count) {
		this.rec_count = rec_count;
	}
	
}
