package bps.classreview.vo;

public class ClassReviewVO {
    private int crev_no;
    private String crev_date;
    private String crev_con;
    private int crev_score;
    private String mem_id;
    private int class_no;
    
    
    
    public int getCrev_no() {
        return crev_no;
    }
    public void setCrev_no(int crev_no) {
        this.crev_no = crev_no;
    }
    public String getCrev_date() {
        return crev_date;
    }
    public void setCrev_date(String crev_date) {
        this.crev_date = crev_date;
    }
    public String getCrev_con() {
        return crev_con;
    }
    public void setCrev_con(String crev_con) {
        this.crev_con = crev_con;
    }
    public int getCrev_score() {
        return crev_score;
    }
    public void setCrev_score(int crev_score) {
        this.crev_score = crev_score;
    }
    public String getMem_id() {
        return mem_id;
    }
    public void setMem_id(String mem_id) {
        this.mem_id = mem_id;
    }
    public int getClass_no() {
        return class_no;
    }
    public void setClass_no(int class_no) {
        this.class_no = class_no;
    }
    
}
